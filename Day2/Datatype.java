class Datatype{
	public static void main ( String args[]){
		
		byte by;
		by = 127;
		System.out.println("Byte no is "+by);
		
		short sh;
		sh = 32000;
		System.out.println("Short no is "+sh);
		
		int num;
		num = 100000;
		System.out.println("Int no is "+num);
		
		float fl;
		fl = 15.25F;
		System.out.println("Float no is "+fl);
		
		double d1;
		d1 = 1.1200000457;
		System.out.println("Float no is "+d1);
		
		boolean b1;
		b1=true;
		System.out.println(b1);
		
		char ch1;
		ch1 = 'A';
		System.out.println(ch1);
		
		char ch2; //decimal num
		ch2 = 65;
		System.out.println(ch2);
		
		char ch3; //octal
		ch3 = 0101; 
		System.out.println(ch3);
		
		char ch4; //hexadecimal
		ch4 = 0x41;
		System.out.println(ch4);
		
		char ch5;  //binary
		ch5 = 0b1000001;
		System.out.println(ch5);
		
		char ch6;   //uni code
		ch6 ='\u0041';
		System.out.println(ch6);
		
		
		double d2;
		d2 = 'A';
		System.out.println(d2);
		
		int d3;
		d3 = 'B';
		System.out.println(d3);
		
		double d4;
		d4 = '\u0041'; //uni code
		System.out.println(d4);
		
		double d5;
		d5 = 0b1000001; //binary
		System.out.println(d5);
		

		
	}
}