class Ex1{
	public static void main(String args[]){
		
		int x=5;
		
		System.out.println(x++); //5
		System.out.println(++x); //7
		System.out.println(x--); //7
		System.out.println(--x); //5
		
		x+=5;
		System.out.println(x); //10
		
		x-=3;
		System.out.println(x); //7
		
		x*=8;
		System.out.println(x); //56
		
		x/=6;
		System.out.println(x); //9
		
		x%=6;
		System.out.println(x); //3
		
	}
}