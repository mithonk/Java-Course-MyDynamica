class Variable1{
	public static void main(String args[]){
		
		int x1; //decimal value
		x1 = 65;
		System.out.println(x1);
		
		int x2; //octal value
		x2 = 0101;
		System.out.println(x2);
		
		int x3; //hexadecimal value
		x3 = 0x41;
		System.out.println(x3);
		
		int x4; //binary value
		x4 = 0b1000001;
		System.out.println(x4);
		
		int x5; //unicode value
		x5 = '\u0041';
		System.out.println(x5);
		
	}
}